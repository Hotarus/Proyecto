package Reportes;

/**
 *
 * @author enzun
 */
public class listaproveedor {
    private String ID,nombre,telefono,ciudad,pais,correo;

    public listaproveedor(String ID, String nombre, String telefono, String ciudad, String pais, String correo) {
        this.ID = ID;
        this.nombre = nombre;
        this.telefono = telefono;
        this.ciudad = ciudad;
        this.pais = pais;
        this.correo = correo;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }
    
}
